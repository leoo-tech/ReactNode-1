/*operadores relacionais

    < menor que
    > maior que
    <= menor ou igual
    >= maior ou igual
    == igual
    === igual e mesmo tipo
    != diferente
    !== diferente e mesmo tipo

*/
let a = 3;
let b = 2;

console.log(a > b);
console.log(a < b);
console.log(a >= b);
console.log(a <= b);
console.log(a == b);
console.log(a === b);
console.log(a != b);
console.log(a !== b);
